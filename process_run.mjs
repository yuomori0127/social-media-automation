async function writeRow(rowNum, noteTitle, xLongPost, noteOutline, noteFull, bannerPath = '') {
  // F〜J列を書き込み
  // F=noteタイトル, G=X長文ポスト, H=noteタイトル+見出し, I=note全文, J=バナー画像パス
  const contentRes = await fetch(
    `https://sheets.googleapis.com/v4/spreadsheets/${SPREADSHEET_ID}/values/F${rowNum}:J${rowNum}?valueInputOption=RAW`,
    {
      method: 'PUT',
      headers: { Authorization: `Bearer ${token}`, 'Content-Type': 'application/json' },
      body: JSON.stringify({ range: `F${rowNum}:J${rowNum}`, majorDimension: 'ROWS', values: [[noteTitle, xLongPost, noteOutline, noteFull, bannerPath]] }),
    }
  );
  const cd = await contentRes.json();
  if (!cd.updatedCells) { console.error(`  ❌ コンテンツ書き込みエラー:`, cd); return false; }

  // E列を「処理済」に更新
  const statusRes = await fetch(
    `https://sheets.googleapis.com/v4/spreadsheets/${SPREADSHEET_ID}/values/E${rowNum}?valueInputOption=RAW`,
    {
      method: 'PUT',
      headers: { Authorization: `Bearer ${token}`, 'Content-Type': 'application/json' },
      body: JSON.stringify({ range: `E${rowNum}`, majorDimension: 'ROWS', values: [['処理済']] }),
    }
  );
  const sd = await statusRes.json();
  if (!sd.updatedCells) { console.error(`  ❌ ステータス更新エラー:`, sd); return false; }
  return true;
}

