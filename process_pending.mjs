const rowNums = Object.keys(CONTENT).map(Number).sort((a, b) => a - b);
console.log(`処理対象: ${rowNums.length}行 (行${rowNums[0]}〜行${rowNums[rowNums.length - 1]})\n`);

for (const rowNum of rowNums) {
  const [noteTitle, xLongPost, noteOutline, noteFull, bannerPath = ''] = CONTENT[rowNum];
  console.log(`▶ 行${rowNum} を処理中...`);

  // F列〜J列（コンテンツ）を書き込み
  // F=noteタイトル, G=X長文ポスト, H=noteタイトル+見出し, I=note全文, J=バナー画像パス
  const contentRes = await fetch(
    `https://sheets.googleapis.com/v4/spreadsheets/${SPREADSHEET_ID}/values/F${rowNum}:J${rowNum}?valueInputOption=RAW`,
    {
      method: 'PUT',
      headers: {
        Authorization: `Bearer ${token}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        range: `F${rowNum}:J${rowNum}`,
        majorDimension: 'ROWS',
        values: [[noteTitle, xLongPost, noteOutline, noteFull, bannerPath]],
      }),
    }
  );
  const contentData = await contentRes.json();
  if (contentData.updatedCells) {
    console.log(`  ✅ F〜J列: ${contentData.updatedCells}セル書き込み完了`);
  } else {
    console.error(`  ❌ エラー:`, JSON.stringify(contentData));
    continue;
  }

  // E列（ステータス）を「処理済」に更新
  const statusRes = await fetch(
    `https://sheets.googleapis.com/v4/spreadsheets/${SPREADSHEET_ID}/values/E${rowNum}?valueInputOption=RAW`,
    {
      method: 'PUT',
      headers: {
        Authorization: `Bearer ${token}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        range: `E${rowNum}`,
        majorDimension: 'ROWS',
        values: [['処理済']],
      }),
    }
  );
  const statusData = await statusRes.json();
  if (statusData.updatedCells) {
    console.log(`  ✅ E列: ステータスを「処理済」に更新`);
  } else {
    console.error(`  ❌ ステータス更新エラー:`, JSON.stringify(statusData));
  }

  // API レート制限対策
  await new Promise(r => setTimeout(r, 300));
}

