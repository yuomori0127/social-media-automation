oauth2Client.setCredentials(tokens);
const { token } = await oauth2Client.getAccessToken();

const rowNums = Object.keys(NOTE_FULL).map(Number).sort((a, b) => a - b);
console.log(`処理対象: ${rowNums.length}行 → I列（note全文・Markdown）を書き込み\n`);

for (const rowNum of rowNums) {
  const noteText = NOTE_FULL[rowNum];
  process.stdout.write(`▶ 行${rowNum} を書き込み中...`);

  const res = await fetch(
    `https://sheets.googleapis.com/v4/spreadsheets/${SPREADSHEET_ID}/values/I${rowNum}?valueInputOption=RAW`,
    {
      method: 'PUT',
      headers: {
        Authorization: `Bearer ${token}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        range: `I${rowNum}`,
        majorDimension: 'ROWS',
        values: [[noteText]],
      }),
    }
  );
  const data = await res.json();

  if (data.updatedCells) {
    console.log(` ✅ 完了（${noteText.length}文字）`);
  } else {
    console.error(` ❌ エラー:`, JSON.stringify(data));
  }

  await new Promise(r => setTimeout(r, 300));
}

console.log('\n🎉 全12行のnote全文（Markdown）書き込み完了！');
